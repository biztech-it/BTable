		html = "<a href='#' class='filtersTitle' data-qn=''>" + $.i18n.prop("filters_display_title") + "</a>:&nbsp;&nbsp;<img src='" + bt.helpers.general.getImgDirPath() + prefix + "locked.png' class='" + prefix + "lockedIcon' />" + html;
				
